    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        //Submit l'information a la bse de donn√© sql
        conn= UserDao.ConnectDB();
        String Sql="Select * from utilisateur where (LOGIN=? and PASSWORD=?) AND (TYPE_UTILISATEUR = 'technicien' or TYPE_UTILISATEUR ='techicien')";
        try{
            pst=conn.prepareStatement(Sql);
            pst.setString(1,txtusername.getText());
            pst.setString(2,txtpassword.getText());
            rs=pst.executeQuery();
            if (rs.next()){
                JOptionPane.showMessageDialog(null,"Welcome user");
                setVisible(false);
                dispose();
                MainWindow m=new MainWindow(txtusername.getText(),txtpassword.getText());
                m.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Invalid username or password","Acess Denied",JOptionPane.ERROR_MESSAGE);
                
            }
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null,e);
            
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    
